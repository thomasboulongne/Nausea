, se sentait de trop par rapport aux autres. De trop. c'était le seul rapport que je pusse établir entre ces arbres, ces grilles, ces cailloux. En vain cherchais-je à compter les marronniers, et les situer par rapport à la Velléda, à comparer leur hauteur avec celle des platanes : chacun d'eux s'échappait des relations où je cherchais à l'enfermer, s'isolait, débordait. Ces relations (que je m'obstinais à maintenir pour retarder l'écroulement du monde humain, des mesures, des quantités, des directions) j'en sentais l'arbitraire ; elles ne mordaient plus sur les choses. De trop, le marronnier, là en face de moi un peu sur la gauche. De trop la Velléda...
